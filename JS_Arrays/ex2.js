// How to initialize an array in JavaScript ?
// Method 1: Using an array as literal
const sports = ["crickets", "football", "competitive-programming"];
console.log("sports", sports);

const myArray = [];
console.log("myArray= ", myArray);

const score = [420, 10, 1, 12, 102];
console.log("score= ", score);

// Method 2: Using an array as an objectArray() constructor
const sport = new Array("cricket", "football", "basketball");
console.log("sports= ", sport);

const myArr = new Array();
console.log("myArr= ", myArr);

const scores = new Array(1,4,3,6,7);
console.log("scores= ", scores);